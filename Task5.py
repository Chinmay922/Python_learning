import numpy as np

a = np.array([2, 6, 4, 8, 7])
b = np.array([9, 1, 7, 8, 10])
print("Adding a & b element wise results: ")
print(np.add(a, b))
print("\n")
print("Subtracting a & b element wise results: ")
print(np.subtract(a, b))
print("\n")
print("Multiplying a & b element wise results: ")
print(np.multiply(a, b))
print("\n")
print("Diving a & b element wise results: ")
print(np.divide(a, b))
print("\n")
